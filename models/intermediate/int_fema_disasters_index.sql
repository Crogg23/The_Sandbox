FEMA_DI.DISASTER_ID,
FEMA_DI.FEMA_DISASTER_DECLARATION_ID,
FEMA_DI.DISASTER_DECLARATION_REQUEST_ID,
FEMA_DI.DISASTER_DECLARATION_NAME,
FEMA_DI.DISASTER_DECLARATION_TYPE,
FEMA_DI.DISASTER_DECLARATION_DATE,
FEMA_DI.DISASTER_TYPE,
FEMA_DI.DISASTER_BEGIN_DATE,
FEMA_DI.DISASTER_END_DATE,
FEMA_MI.COUNTY_GEO_ID as County_GeoId_FEMA
from {{ref('stg_fema_disaster_declaration_index') }} as FEMA_DI
    left join {{ref('stg_fema_disaster_declaration_areas_index') }} as FEMA_MI on 
        FEMA_DI.DISASTER_ID = FEMA_MI.DISASTER_ID
   