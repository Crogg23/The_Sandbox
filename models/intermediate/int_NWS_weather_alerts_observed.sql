select
    NWSWAE.COUNTY_GEO_ID,
    NWSWAE.NWS_ALERT_ID,
    NWSWAE.ALERT_STATUS,
    NWSWAE.ALERT_TYPE,
    NWSWAE.NWS_REPORTER,
    NWSWAE.ALERT_TITLE,
    NWSWAE.EVENT_TYPE,
    NWSWAE.EVENT_SEVERITY,
    NWSWAE.EVENT_URGENCY,
    NWSWAE.SENT_TIMESTAMP,
    NWSWAE.EFFECTIVE_TIMESTAMP,
    NWSWAE.EXPIRATION_TIMESTAMP,
    NWSWAE.ONSET_TIMESTAMP
FROM {{ref('stg_nws_weather_alert_events') }} as NWSWAE
where
    ALERT_STATUS = 'Actual'
    and ALERT_TYPE = 'Alert'
    and EVENT_CERTAINTY = 'Observed'